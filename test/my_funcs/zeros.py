def zeros(n: int):
    str_num = str(factor[n - 1])
    count_zeros = 0

    for i in range(len(str_num), 0, -1):
        if str_num[i - 1] == '0':
            count_zeros += 1
            continue
        else:
            break
    return count_zeros


